"use client";
import { useState, useEffect } from "react";
import { useRouter } from "next/navigation";

const DAYS = ["day1","day2","day3","day4","day5","day6","day7"];
const DAY_LABELS = ["Monday","Tuesday","Wednesday","Thursday","Friday","Saturday","Sunday"];

export default function EvaluationPage() {
  const router = useRouter();
  const [data, setData] = useState<any>(null);

  useEffect(() => {
    const d = localStorage.getItem("approvedPlan");
    if (d) setData(JSON.parse(d));
  }, []);

  const verdict = data?.plan?.verdict || "CAUTION";
  const audit = data?.audit || {};
  const ev = data?.eval || {};
  const patient = data?.patient;
  const plan = data?.plan;
  const hasData = !!data;

  const verdictStyle = (v: string) => {
    if (v === "SAFE") return { color: "#16a34a", bg: "#f0fdf4", border: "#bbf7d0" };
    if (v === "UNSAFE") return { color: "#dc2626", bg: "#fef2f2", border: "#fecaca" };
    return { color: "#d97706", bg: "#fffbeb", border: "#fde68a" };
  };
  const vs = verdictStyle(verdict);

  // ── Download as plain text ────────────────────────────────────
  const handleDownload = () => {
    if (!data) return;

    const lines: string[] = [];
    lines.push("=".repeat(60));
    lines.push("NUTRIGUARD PRO — 7-DAY MEAL PLAN REPORT");
    lines.push("=".repeat(60));
    lines.push("");

    if (patient) {
      lines.push("PATIENT DETAILS");
      lines.push("-".repeat(40));
      lines.push(`Name          : ${patient.name}`);
      lines.push(`Age           : ${patient.age} years`);
      lines.push(`Weight        : ${patient.weight} kg`);
      lines.push(`Protein Req.  : ${patient.protein_req} g/day`);
      lines.push(`Region        : ${patient.state_name}`);
      lines.push(`Allergies     : ${patient.allergies || "None"}`);
      lines.push("");
    }

    lines.push("VERDICT");
    lines.push("-".repeat(40));
    lines.push(`Status        : ${verdict}`);
    if (plan?.summary) lines.push(`Summary       : ${plan.summary}`);
    if (plan?.protein_note) lines.push(`Supplement    : ${plan.protein_note}`);
    lines.push("");

    lines.push("AUDIT RESULT");
    lines.push("-".repeat(40));
    lines.push(`Status        : ${audit.status || "APPROVED"}`);
    lines.push(`Message       : ${audit.message || "Plan is clinically safe."}`);
    if (audit.flags?.length > 0) {
      lines.push(`Flagged items : ${audit.flags.join(", ")}`);
    }
    lines.push("");

    lines.push("PIPELINE TELEMETRY");
    lines.push("-".repeat(40));
    lines.push(`Model used    : ${ev.model_used || "gemini-2.5-flash"}`);
    lines.push(`Chef retries  : ${ev.iterations ?? 0}`);
    lines.push(`Fallback used : ${ev.used_fallback ? "Yes" : "No"}`);
    lines.push(`RAG flags     : ${ev.flag_count ?? 0}`);
    lines.push("");

    if (plan?.plan) {
      lines.push("7-DAY MEAL PLAN");
      lines.push("=".repeat(60));
      DAYS.forEach((d, i) => {
        const dayPlan = plan.plan[d];
        if (!dayPlan) return;
        lines.push("");
        lines.push(DAY_LABELS[i].toUpperCase());
        lines.push("-".repeat(40));
        lines.push(`Breakfast : ${dayPlan.breakfast || "—"}`);
        lines.push(`Lunch     : ${dayPlan.lunch || "—"}`);
        lines.push(`Dinner    : ${dayPlan.dinner || "—"}`);
        lines.push(`Snack     : ${dayPlan.snack || "—"}`);
      });
      lines.push("");
    }

    lines.push("=".repeat(60));
    lines.push("Generated by NutriGuard Pro — AI Nutrition Planning for IBD");
    lines.push("=".repeat(60));

    const blob = new Blob([lines.join("\n")], { type: "text/plain" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = `nutriguard_${patient?.name?.replace(/\s+/g, "_") || "plan"}_${new Date().toISOString().slice(0,10)}.txt`;
    a.click();
    URL.revokeObjectURL(url);
  };

  // ── Telemetry cards ───────────────────────────────────────────
  const telemetryCards = hasData
    ? [
        {
          label: "Chef Retries",
          value: ev.iterations === 0 ? "None" : `${ev.iterations} retry`,
          sub: ev.iterations === 0
            ? "Plan passed audit on first attempt"
            : "Chef corrected plan after Auditor rejection",
          color: ev.iterations === 0 ? "#16a34a" : "#d97706",
          bg: ev.iterations === 0 ? "#f0fdf4" : "#fffbeb",
          border: ev.iterations === 0 ? "#bbf7d0" : "#fde68a",
        },
        {
          label: "Gemini Model",
          value: ev.model_used || "gemini-2.5-flash",
          sub: ev.used_fallback
            ? "Fallback chain activated — primary key was rate-limited"
            : "Primary model responded — no fallback needed",
          color: ev.used_fallback ? "#d97706" : "#2563eb",
          bg: ev.used_fallback ? "#fffbeb" : "#eff6ff",
          border: ev.used_fallback ? "#fde68a" : "#bfdbfe",
        },
        {
          label: "RAG Audit Flags",
          value: `${ev.flag_count ?? audit.flags?.length ?? 0} found`,
          sub: audit.status === "APPROVED"
            ? "No unsafe items detected in clinical PDF scan"
            : `Unsafe items: ${(audit.flags || []).join(", ")}`,
          color: (ev.flag_count ?? 0) === 0 ? "#16a34a" : "#dc2626",
          bg: (ev.flag_count ?? 0) === 0 ? "#f0fdf4" : "#fef2f2",
          border: (ev.flag_count ?? 0) === 0 ? "#bbf7d0" : "#fecaca",
        },
        {
          label: "Final Verdict",
          value: verdict,
          sub: verdict === "SAFE"
            ? "Plan is clinically safe for the patient"
            : verdict === "UNSAFE"
            ? "Plan requires revision before use"
            : "Plan approved with minor caution",
          color: vs.color,
          bg: vs.bg,
          border: vs.border,
        },
      ]
    : [];

  const techStack = [
    {
      category: "Orchestration",
      items: [
        "LangGraph — sequential workflow graph",
        `Conditional retry: Chef retried ${ev.iterations ?? "—"} time(s)`,
        "State persistence across all 4 nodes",
      ],
      accent: "#7c3aed",
    },
    {
      category: "LLM & Fallback Chain",
      items: [
        `Active model: ${ev.model_used || "gemini-2.5-flash"}`,
        ev.used_fallback ? "Fallback triggered this run" : "Primary model handled this run",
        "Chain: 2.5-flash → 2.0-flash → 2.5-flash-lite",
      ],
      accent: "#2563eb",
    },
    {
      category: "Vector Engine (RAG)",
      items: [
        "Fine-tuned MiniLM-L6-v2 embeddings",
        "ChromaDB local vector store",
        `Flags detected: ${ev.flag_count ?? audit.flags?.length ?? 0} item(s)`,
      ],
      accent: "#16a34a",
    },
    {
      category: "Research Pipeline",
      items: [
        "Tavily Search API (3 queries/state)",
        "SQLite knowledge graph — regional foods",
        "Gemini extraction — 30+ food nodes",
      ],
      accent: "#d97706",
    },
    {
      category: "Infrastructure",
      items: [
        "FastAPI backend — Docker on Hugging Face",
        "Next.js frontend — Vercel",
        "Server-Sent Events (SSE) live streaming",
      ],
      accent: "#dc2626",
    },
  ];

  return (
    <div className="min-h-screen bg-gray-50">
      {/* Top bar */}
      <div className="bg-white border-b border-gray-200 px-6 py-4 flex items-center justify-between">
        <div>
          <h1 className="text-lg font-bold text-gray-900">Evaluation</h1>
          <p className="text-xs text-gray-400 mt-0.5">Pipeline telemetry · Audit result · Architecture</p>
        </div>
        <button
          onClick={() => router.push("/")}
          className="text-sm text-gray-500 hover:text-gray-800 border border-gray-200 px-3 py-1.5 rounded-lg transition bg-white"
        >
          ← Home
        </button>
      </div>

      <div className="max-w-5xl mx-auto px-6 py-6 space-y-5">

        {/* Patient + verdict */}
        {patient && (
          <div className="bg-white rounded-2xl border border-gray-200 shadow-sm p-4 flex flex-wrap items-center justify-between gap-4">
            <div>
              <p className="text-xs text-gray-400 mb-1">Approved plan for</p>
              <p className="text-sm font-semibold text-gray-900">
                {patient.name} · {patient.age} years · {patient.state_name}
              </p>
              {plan?.summary && (
                <p className="text-xs text-gray-500 mt-1 max-w-lg leading-relaxed">{plan.summary}</p>
              )}
            </div>
            <div className="flex flex-col items-end gap-2">
              <span
                className="text-sm font-bold px-4 py-1.5 rounded-full border"
                style={{ color: vs.color, background: vs.bg, borderColor: vs.border }}
              >
                {verdict}
              </span>
              <button
                onClick={handleDownload}
                className="text-xs px-4 py-2 rounded-xl font-semibold text-white bg-blue-600 hover:bg-blue-700 transition flex items-center gap-1.5"
              >
                Download Plan as Text
              </button>
            </div>
          </div>
        )}

        {/* No data notice */}
        {!hasData && (
          <div className="bg-white rounded-2xl border border-gray-200 shadow-sm p-8 text-center">
            <p className="text-sm text-gray-400 mb-3">No approved plan found.</p>
            <button
              onClick={() => router.push("/generate")}
              className="text-sm px-5 py-2.5 rounded-xl font-semibold text-white bg-purple-600 hover:bg-purple-700 transition"
            >
              Generate a Plan First
            </button>
          </div>
        )}

        {/* Telemetry cards — 4 factual cards, no fake scores */}
        {telemetryCards.length > 0 && (
          <div>
            <p className="text-sm font-bold text-gray-700 mb-3">Pipeline Telemetry</p>
            <div className="grid grid-cols-2 lg:grid-cols-4 gap-3">
              {telemetryCards.map(c => (
                <div
                  key={c.label}
                  className="bg-white rounded-xl border shadow-sm p-4"
                  style={{ borderColor: c.border }}
                >
                  <p className="text-xs font-semibold text-gray-400 uppercase tracking-wider mb-2">{c.label}</p>
                  <p className="text-base font-extrabold mb-1" style={{ color: c.color }}>{c.value}</p>
                  <p className="text-xs text-gray-400 leading-relaxed">{c.sub}</p>
                </div>
              ))}
            </div>
          </div>
        )}

        {/* Audit result */}
        {audit.status && (
          <div className="bg-white rounded-2xl border border-gray-200 shadow-sm overflow-hidden">
            <div className="px-5 py-3.5 border-b border-gray-100">
              <p className="text-sm font-bold text-gray-800">Clinical Audit Result</p>
            </div>
            <div className="p-5">
              <div className="flex items-center gap-3 mb-3">
                <span
                  className="text-sm font-bold px-3 py-1 rounded-full border"
                  style={{
                    color: audit.status === "APPROVED" ? "#16a34a" : "#dc2626",
                    background: audit.status === "APPROVED" ? "#f0fdf4" : "#fef2f2",
                    borderColor: audit.status === "APPROVED" ? "#bbf7d0" : "#fecaca",
                  }}
                >
                  {audit.status}
                </span>
                {audit.flags?.length > 0 && (
                  <span className="text-xs text-gray-400">{audit.flags.length} flag(s) detected</span>
                )}
              </div>
              <p className="text-sm text-gray-600">{audit.message}</p>
              {audit.flags?.length > 0 && (
                <div className="mt-3 flex flex-wrap gap-2">
                  {audit.flags.map((f: string) => (
                    <span key={f} className="text-xs px-2.5 py-1 rounded-full bg-red-50 text-red-600 border border-red-200">
                      {f}
                    </span>
                  ))}
                </div>
              )}
            </div>
          </div>
        )}

        {/* Tech stack */}
        <div>
          <p className="text-sm font-bold text-gray-800 mb-3">Tech Stack</p>
          <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-3">
            {techStack.map(t => (
              <div
                key={t.category}
                className="bg-white rounded-xl border border-gray-200 shadow-sm p-4"
                style={{ borderLeft: `3px solid ${t.accent}` }}
              >
                <p className="text-xs font-bold uppercase tracking-wider mb-2.5" style={{ color: t.accent }}>
                  {t.category}
                </p>
                {t.items.map(item => (
                  <p key={item} className="text-xs text-gray-500 mb-1.5 leading-relaxed">{item}</p>
                ))}
              </div>
            ))}
          </div>
        </div>

        {/* Architecture note */}
        <div className="bg-white rounded-2xl border border-gray-200 shadow-sm p-5">
          <p className="text-sm font-bold text-gray-800 mb-3">Architecture Note</p>
          <p className="text-sm text-gray-500 leading-relaxed">
            NutriGuard Pro uses a{" "}
            <span className="font-medium text-purple-600">sequential multi-agent graph</span> via LangGraph.
            The Researcher populates a{" "}
            <span className="font-medium text-amber-600">SQLite knowledge graph</span> via Tavily. The Chef
            generates a plan which the{" "}
            <span className="font-medium text-green-600">RAG Auditor</span> validates against clinical PDFs using
            a fine-tuned MiniLM model in ChromaDB. If rejected, the Chef retries once.
            A <span className="font-medium text-blue-600">3-model fallback chain</span> with{" "}
            <span className="font-medium text-blue-600">dual API key rotation</span> handles quota errors
            automatically. All telemetry on this page reflects the actual execution of this pipeline run.
          </p>
        </div>

        {/* Actions */}
        <div className="flex gap-3">
          <button
            onClick={handleDownload}
            disabled={!hasData}
            className="flex-1 py-3 rounded-xl text-white text-sm font-bold bg-blue-600 hover:bg-blue-700 disabled:bg-gray-300 disabled:cursor-not-allowed transition"
          >
            Download Plan as Text
          </button>
          <button
            onClick={() => router.push("/generate")}
            className="flex-1 py-3 rounded-xl text-white text-sm font-bold bg-purple-600 hover:bg-purple-700 transition"
          >
            ← Generate New Plan
          </button>
          <button
            onClick={() => router.push("/")}
            className="flex-1 py-3 rounded-xl text-sm font-semibold text-gray-600 bg-white border border-gray-200 hover:bg-gray-50 transition"
          >
            Home
          </button>
        </div>
      </div>
    </div>
  );
}
